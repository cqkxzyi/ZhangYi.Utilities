        /// <summary>
        /// 获取文件夹下所有文件大小
        /// </summary>
        /// <param name="direcPath"></param>
        /// <returns></returns>
        public static long getDirSize(DirectoryInfo direcPath)
        {
            long Size = 0;// 所有文件大小.
            try
            {
                FileInfo[] fis = direcPath.GetFiles();
                foreach (FileInfo fi in fis)
                {
                    Size += fi.Length;
                }
                // 遍历出当前目录的所有文件夹.
                DirectoryInfo[] dis = direcPath.GetDirectories();
                foreach (DirectoryInfo di in dis)
                {
                    Size += getDirSize(di);   //这就用到递归了，调用父方法,注意，这里并不是直接返回值，而是调用父返回来的
                }
            }
            catch { Size = 0; }
            return (Size);
        }
