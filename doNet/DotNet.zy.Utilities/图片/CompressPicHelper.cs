        public enum ThumbnailMod : byte
        {
            /// <summary>
            /// HW 指定高宽缩放（可能变形）
            /// </summary>
            HW,
            /// <summary>
            /// W 指定宽，高按比例    
            /// </summary>
            W,
            /// <summary>
            /// H 指定高，宽按比例
            /// </summary>
            H,
            /// <summary>
            /// Cut 指定高宽裁减（不变形）
            /// </summary>
            Cut
        }
