        #region 将图片压缩到指定尺寸的图片
        /// <summary>
        /// 将图片压缩到指定尺寸的图片
        /// </summary>
        /// <param name="strSrcFile">原文件</param>
        /// <param name="strTargetFile">新文件</param>
        /// <param name="Width">生成图片宽度</param>
        /// <param name="Height">生成图片高度</param>
        /// <returns></returns>
        public static bool CompressPic(string strSrcFile, string strTargetFile, int Width, int Height)
        {
            try
            {
                Image img = Image.FromFile(strSrcFile);

                Size newSize = new Size(Width, Height);
                Bitmap outBmp = new Bitmap(newSize.Width, newSize.Height);

                Graphics g = Graphics.FromImage(outBmp);
                g.InterpolationMode = InterpolationMode.HighQualityBicubic;
                g.CompositingQuality = CompositingQuality.HighQuality;
                g.SmoothingMode = SmoothingMode.HighQuality;
                g.DrawImage(img, new Rectangle(0, 0, newSize.Width, newSize.Height), 0, 0, img.Width, img.Height, GraphicsUnit.Pixel);
                g.Dispose();

                //压缩质量，越大照片越清晰
                EncoderParameters encoderParams = new EncoderParameters();
                long[] quality = new long[1];
                quality[0] = 80;
                EncoderParameter encoderParam = new EncoderParameter(System.Drawing.Imaging.Encoder.Quality, quality);
                encoderParams.Param[0] = encoderParam;

                img.Dispose();

                string mimeType = "image/jpeg";

                string strFileExtension = Path.GetExtension(strSrcFile).ToLower();
                switch (strFileExtension)
                {
                    case ".gif":
                        mimeType = "image/gif";
                        break;
                    case ".bmp":
                        mimeType = "image/x-ms-bmp";
                        break;
                    case ".png":
                        mimeType = "image/png";
                        break;
                    case ".tiff":
                    case ".tif":
                        mimeType = "image/tiff";
                        break;
                }

                outBmp.Save(strTargetFile, GetImageCodecInfo(mimeType), encoderParams);
                outBmp.Dispose();
                return true;
            }
            catch
            {
                return false;
            }
        }
